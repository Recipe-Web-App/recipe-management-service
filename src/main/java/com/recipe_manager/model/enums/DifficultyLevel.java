package com.recipe_manager.model.enums;

/**
 * Enum representing the difficulty level of a recipe. Maps to the difficulty_level_enum in the
 * database.
 */
public enum DifficultyLevel {
  /** Suitable for complete beginners. */
  BEGINNER,
  /** Easy difficulty. */
  EASY,
  /** Medium difficulty. */
  MEDIUM,
  /** Hard difficulty. */
  HARD,
  /** Expert difficulty. */
  EXPERT
}
