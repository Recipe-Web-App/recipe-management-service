package com.recipe_manager.model.enums;

/**
 * Enum representing the unit of measurement for ingredients. Maps to the ingredient_unit_enum in
 * the database.
 */
public enum IngredientUnit {
  /** Gram. */
  G,
  /** Kilogram. */
  KG,
  /** Ounce. */
  OZ,
  /** Pound. */
  LB,
  /** Milliliter. */
  ML,
  /** Liter. */
  L,
  /** Cup. */
  CUP,
  /** Tablespoon. */
  TBSP,
  /** Teaspoon. */
  TSP,
  /** Piece. */
  PIECE,
  /** Clove (garlic, etc.). */
  CLOVE,
  /** Slice. */
  SLICE,
  /** Pinch. */
  PINCH,
  /** Can. */
  CAN,
  /** Bottle. */
  BOTTLE,
  /** Packet. */
  PACKET,
  /** Unit. */
  UNIT
}
